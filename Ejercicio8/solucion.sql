SELECT  CODIGO_FACTURA,
        TO_CHAR(FECHA_INICIO, 'DD-MONTH-YYYY') AS FECHA_PAGO,
        TO_CHAR(FECHA_INICIO, 'YYYY') AS AÑO_PAGO,
        TO_CHAR(FECHA_INICIO, 'MONTH') AS MES_PAGO,
        ROUND(MONTO, 2) AS MONTO,
        ROUND(DESCUENTO, 2) AS DESCUENTO
FROM TBL_FACTURACION_PAGOS;  

CREATE MATERIALIZED VIEW MVW_REGISTRO_FACTURACIONES AS
WITH USUARIOS_PLANES AS (
    SELECT  CODIGO_USUARIO,
            NOMBRE || ' ' || APELLIDO AS NOMBRE_COMPLETO,
            NOMBRE_PLAN
    FROM TBL_USUARIOS A
    LEFT JOIN TBL_PLANES B
    ON A.CODIGO_PLAN = B.CODIGO_PLAN
    ORDER BY CODIGO_USUARIO
), 
DATOS_FACTURACION AS (
    SELECT  CODIGO_FACTURA,
            CODIGO_USUARIO,
            TO_CHAR(FECHA_INICIO, 'DD-FMMONTH-YYYY') AS FECHA_PAGO,
            TO_CHAR(FECHA_INICIO, 'YYYY') AS AÑO_PAGO,
            TO_CHAR(FECHA_INICIO, 'MONTH') AS MES_PAGO,
            ROUND(MONTO, 2) AS MONTO,
            ROUND(DESCUENTO, 2) AS DESCUENTO,
            ROUND((MONTO-DESCUENTO), 2) AS TOTAL_NETO
    FROM TBL_FACTURACION_PAGOS
)
SELECT  B.CODIGO_FACTURA,
        A.NOMBRE_PLAN,
        A.NOMBRE_COMPLETO,
        B.FECHA_PAGO,
        B.AÑO_PAGO,
        B.MES_PAGO,
        B.MONTO,
        B.DESCUENTO,
        B.TOTAL_NETO --IMPORTANTE COLOCAR LOS CAMPOS EXPLICITOS, YA QUE ORACLE NO SOPORTA EL SELECT * A LA HORA DE CREAR VISTAS MATERIALIZADAS
FROM USUARIOS_PLANES A
INNER JOIN DATOS_FACTURACION B
ON A.CODIGO_USUARIO = B.CODIGO_USUARIO
ORDER BY CODIGO_FACTURA;

COMMIT;

SELECT *
FROM MVW_REGISTRO_FACTURACIONES
ORDER BY CODIGO_FACTURA;



