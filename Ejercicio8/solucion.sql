SELECT *
FROM TBL_ORGANIZACIONES;

--DE ESTA CONSULTA SACAMOS LA CANTIDAD DE USUARIOS POR ORGANIZACION
SELECT  CODIGO_ORGANIZACION, COUNT(*) AS CANTIDAD_USUARIOS
FROM TBL_USUARIOS_X_ORGANIZACION
GROUP BY CODIGO_ORGANIZACION;

--DE ESTA CONSULTA SACAMOS LA CANTIDAD DE TABLEROS POR ORGANIZACION
SELECT  CODIGO_ORGANIZACION, COUNT(*) AS CANTIDAD_TABLEROS
FROM TBL_TABLERO 
GROUP BY CODIGO_ORGANIZACION;

--DE ESTA CONSULTA SACAMOS LA CANTIDAD DE LISTAS POR ORGANIZACION, UTILIZANDO UN INNER JOIN PARA RELACIONAR TABLERO CON LISTAS Y PODER OBTENER LA CANTIDAD DE LISTAS
SELECT  CODIGO_ORGANIZACION, COUNT(*) AS CANTIDAD_LISTAS
FROM TBL_TABLERO A 
INNER JOIN TBL_LISTAS B
ON A.CODIGO_TABLERO = B.CODIGO_TABLERO
GROUP BY CODIGO_ORGANIZACION;

--DE ESTA CONSULTA SACAMOS LA CANTIDAD DE TARJETAS POR ORGANIZACION, HACIENDO INNER JOINS CON LAS TABLAS TABLERO, LISTAS Y TARJETAS
SELECT  CODIGO_ORGANIZACION, COUNT(*) AS CANTIDAD_TARJETAS
FROM TBL_TABLERO A
INNER JOIN TBL_LISTAS B
ON A.CODIGO_TABLERO = B.CODIGO_TABLERO
INNER JOIN TBL_TARJETAS C
ON B.CODIGO_LISTA = C.CODIGO_LISTA
GROUP BY CODIGO_ORGANIZACION;

WITH USUARIOS_ORGANIZACIONES AS (
    SELECT  CODIGO_ORGANIZACION, COUNT(*) AS CANTIDAD_USUARIOS
    FROM TBL_USUARIOS_X_ORGANIZACION
    GROUP BY CODIGO_ORGANIZACION
), 
TABLEROS_ORGANIZACIONES AS (
    SELECT  CODIGO_ORGANIZACION, COUNT(*) AS CANTIDAD_TABLEROS
    FROM TBL_TABLERO 
    GROUP BY CODIGO_ORGANIZACION
), 
LISTAS_ORGANIZACIONES AS (
    SELECT  CODIGO_ORGANIZACION, COUNT(*) AS CANTIDAD_LISTAS
    FROM TBL_TABLERO A 
    INNER JOIN TBL_LISTAS B
    ON A.CODIGO_TABLERO = B.CODIGO_TABLERO
    GROUP BY CODIGO_ORGANIZACION
),
TARJETAS_ORGANIZACIONES AS (
    SELECT  CODIGO_ORGANIZACION, COUNT(*) AS CANTIDAD_TARJETAS
    FROM TBL_TABLERO A
    INNER JOIN TBL_LISTAS B
    ON A.CODIGO_TABLERO = B.CODIGO_TABLERO
    INNER JOIN TBL_TARJETAS C
    ON B.CODIGO_LISTA = C.CODIGO_LISTA
    GROUP BY CODIGO_ORGANIZACION
)
SELECT  A.CODIGO_ORGANIZACION,
        NVL(B.CANTIDAD_USUARIOS, 0) AS CANTIDAD_USUARIOS,
        NVL(C.CANTIDAD_TABLEROS, 0) AS CANTIDAD_TABLEROS,
        NVL(D.CANTIDAD_LISTAS, 0) AS CANTIDAD_LISTAS,
        NVL(E.CANTIDAD_TARJETAS, 0) AS CANTIDAD_TARJETAS
FROM TBL_ORGANIZACIONES A
LEFT JOIN USUARIOS_ORGANIZACIONES B
ON A.CODIGO_ORGANIZACION = B.CODIGO_ORGANIZACION
LEFT JOIN TABLEROS_ORGANIZACIONES C
ON B.CODIGO_ORGANIZACION = C.CODIGO_ORGANIZACION
LEFT JOIN LISTAS_ORGANIZACIONES D
ON C.CODIGO_ORGANIZACION = D.CODIGO_ORGANIZACION
LEFT JOIN TARJETAS_ORGANIZACIONES E
ON D.CODIGO_ORGANIZACION = E.CODIGO_ORGANIZACION
GROUP BY A.CODIGO_ORGANIZACION, B.CANTIDAD_USUARIOS, C.CANTIDAD_TABLEROS, D.CANTIDAD_LISTAS, E.CANTIDAD_TARJETAS;



