SELECT  A.CODIGO_FACTURA,
        TO_CHAR(A.FECHA_INICIO, 'DD-MONTH-YYYY') AS FECHA_PAGO,
        TO_CHAR(A.FECHA_INICIO, 'YYYY') AS AÑO_PAGO,
        TO_CHAR(A.FECHA_INICIO, 'MONTH') AS MES_PAGO, 
        B.NOMBRE_PLAN,
        C.NOMBRE || ' ' || C.APELLIDO AS NOMBRE_COMPLETO,
        A.MONTO,
        A.DESCUENTO
FROM TBL_FACTURACION_PAGOS A
INNER JOIN TBL_PLANES B
ON A.CODIGO_PLAN = B.CODIGO_PLAN
INNER JOIN TBL_USUARIOS C
ON A.CODIGO_USUARIO = C.CODIGO_USUARIO
GROUP BY A.CODIGO_FACTURA, B.NOMBRE_PLAN, A.MONTO, A.DESCUENTO
ORDER BY A.CODIGO_FACTURA;

