--1. Desarrollar una consulta para encontrar a los 5 usuarios más activos en cuanto a participación en grupos.
--Se considera participación activa cuando un usuario ha publicado contenido en al menos 3 grupos diferentes durante el último año.
--Mostrar el nombre del usuario, la cantidad de grupos en los que publicó, y el total de publicaciones realizadas en esos grupos.

SELECT *
FROM TBL_GRUPOS;

SELECT *
FROM TBL_PUBLICACIONES;
WHERE CODIGO_USUARIO = 1;

SELECT  A.NOMBRE_USUARIO,
        COUNT(B.CODIGO_GRUPO) AS PARTICIPACION_EN_GRUPOS,
        COUNT(B.CODIGO_PUBLICACION) AS CANTIDAD_PUBLICACIONES
FROM TBL_USUARIOS A
INNER JOIN TBL_PUBLICACIONES B
ON A.CODIGO_USUARIO = B.CODIGO_USUARIO
WHERE B.FECHA_PUBLICACION >= '26-MAR-2025'
HAVING COUNT(B.CODIGO_GRUPO) >= 3
GROUP BY A.NOMBRE_USUARIO;

