--Muestre el título de la foto, el nombre completo del usuario que la subió y el total de interacciones, además mostrar
--el país del usuario, la cantidad de mensajes que ha enviado y la cantidad de mensajes que ha recibido.

SELECT *
FROM TBL_FOTOS;

SELECT *
FROM TBL_LIKE_FOTOGRAFIAS
ORDER BY 2;

SELECT *
FROM TBL_MENSAJES;

--CANTIDAD DE LIKES POR FOTO
SELECT  CODIGO_FOTO,
        COUNT(*) AS CANTIDAD_LIKES
FROM TBL_LIKE_FOTOGRAFIAS
GROUP BY CODIGO_FOTO;

--CANTIDAD DE ETIQUETAS POR FOTOS
SELECT  CODIGO_FOTO,
        COUNT(*) AS CANTIDAD_ETIQUETAS
FROM TBL_ETIQUETA_FOTOGRAFIAS
GROUP BY CODIGO_FOTO;

--USUARIOS QUE HAN SUBIDO FOTOS
SELECT  B.CODIGO_USUARIO,
        C.NOMBRE_USUARIO,
        D.NOMBRE_PAIS,
        A.CODIGO_FOTO
FROM TBL_FOTOS A
INNER JOIN TBL_ALBUMES B
ON A.CODIGO_ALBUM = B.CODIGO_ALBUM
INNER JOIN TBL_USUARIOS C
ON B.CODIGO_USUARIO = C.CODIGO_USUARIO
INNER JOIN TBL_PAISES D
ON C.CODIGO_PAIS = D.CODIGO_PAIS;

--CANTIDAD DE MENSAJES RECIBIDOS
SELECT  A.CODIGO_USUARIO,
        COUNT(*) AS RECIBIDOS
FROM TBL_USUARIOS A 
INNER JOIN TBL_MENSAJES B
ON A.CODIGO_USUARIO = B.CODIGO_USUARIO
GROUP BY A.CODIGO_USUARIO;

--CANTIDAD DE MENSAJES ENVIADOS
SELECT  A.CODIGO_USUARIO,
        COUNT(*) AS ENVIADOS
FROM TBL_USUARIOS A
INNER JOIN TBL_MENSAJES B
ON A.CODIGO_USUARIO = B.CODIGO_REMITENTE
GROUP BY A.CODIGO_USUARIO;

